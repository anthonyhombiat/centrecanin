		<h2>&Eacute;ducation</h2>
		<h3>Une bonne relation passe obligatoirement par une bonne &eacute;ducation</h3>
	</header>

	<img class="float_right paysage_format image_with_frame" src="static/img/education.jpg" alt="education" />

	<p>
		Les cours de dressage ou d'&eacute;ducation (selon la sensibilit&eacute; de chacun !!) se pratiquent en collectif ou en le&ccedil;ons particuli&egrave;res,
		sur un terrain &eacute;quip&eacute; et s&eacute;curis&eacute; ou &agrave; domicile. Les cours d'&eacute;ducations se dispensent tous les jours <strong>sur rendez-vous</strong> de 9h &agrave; 19h sauf les jeudi et dimanche.
	</p>

	<p>
		Rien de plus simple que de dresser son chien lorsque nous sommes seul avec lui, mais d&egrave;s lors qu'il y une distraction certains probl&egrave;mes arrivent,
		vous perdez toute ob&eacute;issance et l'attention de votre compagnon. C'est pour cela que je pratique les le&ccedil;ons d'&eacute;ducation en collectif
		(groupe de deux &agrave; six chiens). Votre chien a besoin de se sociabiliser et accepter d'ob&eacute;ir malgr&eacute; les stimulations environnementales.
	</p>

	<p>
		Le premier rendez-vous se pratique toujours en individuel. Le but est de vous donner un maximum d'informations sur les habitudes de
		vie quotidiennes et de pratiquer le bilan comportemental de votre chien, pour comprendre les difficult&eacute;s que vous pouvez rencontrer
		tous les jours. Et bien s&ucirc;r mettre en place ensemble, le meilleur pour vous et votre compagnon.
	</p>

	<p>
		Par la suite, je vous proposerai donc si n&eacute;cessaire, un contrat d'&eacute;ducation canine adapt&eacute; &agrave; vos besoins et aux capacit&eacute;s du chien.
	</p>

